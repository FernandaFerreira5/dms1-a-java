package fahrenheit;

public class ConversorCelsiusParaFahrenheit {
	
	public static void main(String[] args) {
		
		float C = 27;
		float valor9 = 9;
		float valor160 = 160;
		float valor5 = 5;
		float F = (valor9 * C + valor160) / valor5;
		
		System.out.print("Fahrenheit: ");
		System.out.println(F);
	
	}

}
